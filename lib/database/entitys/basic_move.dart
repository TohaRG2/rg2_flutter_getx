
import 'package:floor/floor.dart';

/// Модель данных для хранения базовых движений (алгоритмов) в локальной базе данных.
/// Используется для хранения информации о движениях кубика, таких как формулы, иконки
/// и всплывающие подсказки.
///
/// Пример использования:
/// ```dart
/// final basicMove = BasicMove(
///   id: 1,
///   eType: 'cross',
///   move: 'R U R\' U\'',
///   icon: 'cross_icon',
///   toast: 'Алгоритм для креста'
/// );
/// ```
@Entity(tableName: 'basic_moves', primaryKeys: ['eType', 'id'])
class BasicMove {
  /// Уникальный идентификатор движения в рамках типа
  final int id;

  /// Тип движения (например, 'cross', 'f2l', 'oll', 'pll')
  /// Используется как часть составного первичного ключа
  final String eType;

  /// Текст движения/алгоритма (например, "R U R' U'")
  String move;

  /// Имя иконки или путь к изображению для визуального представления движения
  String icon;

  /// Текст всплывающей подсказки (toast), отображаемой при взаимодействии
  String toast;

  /// Создает новый экземпляр [BasicMove].
  ///
  /// Параметры:
  /// - [id]: Уникальный идентификатор движения
  /// - [eType]: Тип движения
  /// - [move]: Текст алгоритма (по умолчанию пустая строка)
  /// - [icon]: Имя иконки (по умолчанию пустая строка)
  /// - [toast]: Текст подсказки (по умолчанию пустая строка)
  BasicMove({
    required this.id,
    required this.eType,
    this.move = "",
    this.icon = "",
    this.toast = "",
  });

  @override
  String toString() => "id = $id, eType = $eType, move = $move";
}

