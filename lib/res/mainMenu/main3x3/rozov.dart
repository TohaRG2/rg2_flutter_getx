        <p>Если наклейка на третьем этаже справа, значит это правильная, хорошая рыбка. Поставить ее
        нужно так, чтобы рыбка плыла к нам и налево.</p>
        <p style="text-align:center"><img src="rozov_9_1.xml"></p>

        <p>Как решать: Делаем рыбный пиф-паф, который отличается от простого только направлением
        последнего хода, включаем свет, крышу дважды и выключаем свет.</p>

