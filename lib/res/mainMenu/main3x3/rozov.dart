        <p style="text-align:center"><img src="rozov_9_2.xml"></p>

        <p>Как решать: так же как и Рыбку, но все ходы обратные.</p>

        <p><a href="rg2://ytplay?time=8:28&link=_ZqL3XwyoDE">Алгоритм/фингертрикс:</a> <b>(R\' U\' R U\') R\' U2 R</b></p>

        <p><b>2. <a href="rg2://ytplay?time=9:27&link=_ZqL3XwyoDE">Неверно ориентированы 4 угла.</b></a></p>
        <p>Во обоих этих случаях, с какой-то стороны обязательно есть \"Глазки\", ставим так, чтобы
        \"Глазки\" смотрели налево.</p>

